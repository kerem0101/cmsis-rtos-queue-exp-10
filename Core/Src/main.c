/**
  * @brief  Function implementing the senderTask2 thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_sender */
void sender(void const * argument)
{
  /* USER CODE BEGIN 5 */
  int32_t val;
  osStatus status;

  val = (int32_t) argument;

  /* Infinite loop */
  for(;;)
  {

	status = osMessagePut (queueHandle, val, 100);
    if(osOK != status){
    	//error
    	uartPrint("Could not send to the queue.\r\n");
    }
    osDelay(5000);
  }
  /* USER CODE END 5 */
}

